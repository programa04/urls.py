$(document).ready(function(){
  $(".r1").click(function(){
    $("body").css("background-color", "#008C88");
    $(".fonte_cor").css("color","#008C88");
    $("#final_pagina").removeClass('bg_grad_azul');
    $("#final_pagina").removeClass('bg_grad_roxo');
    $("#final_pagina").removeClass('bg_grad_magenta');
    $("#final_pagina").removeClass('bg_grad_dark_pink');
    $("#final_pagina").removeClass('bg_grad_vivid_red');
    $("#final_pagina").removeClass('bg_grad_vivid_orange');
    $("#final_pagina").removeClass('bg_grad_dark_green');
    $("#final_pagina").addClass("bg_grad_ciano");
  }); 
});

