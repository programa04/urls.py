$(document).ready(function(){
  $("#troca").click(function(){
    $(".Cadastro").hide();
    $("#Sumir").hide();
    $("#troca").hide();
    $("#LogShow").show();
    $("#volta").show();
  });
});

$(document).ready(function(){
  $("#volta").click(function(){
    $("#LogShow").hide();
    $("#volta").hide();
    $("#Sumir").show();
    $(".Cadastro").show();
    $("#troca").show();
  });
});